# Arithmetic Operators:

a=50
b=10

print(a+b)        #addition
print(a-b)        #subtraction
print(a*b)        #multiplication
print(a/b,3/2)    #division
print(a%b)        #modulus/remainder
print(a**2)       #exponents/square
print(a//b,3//2)  #floor division
print("part 1 complete")

