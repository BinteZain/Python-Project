# Logical Operators:

x=10
y=5
print("and")
print(x>y and x<20)                 # (true)
print(x>y and x<20 and x==y)        # (false)

print("or")
print(x>y or x<20)                  # (true)
print(x>y or x<y)                   # (true)
print(x<y or x==y)                  # (false)

print("not")
print(not (x==y) )                  # (true)
print(not (x!=y) )                  # (false)

