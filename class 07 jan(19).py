# Variable Dictionary :

my_dict = {
    "name"     : "Python" ,
    "fees"     : 10000 ,
    "duration" : "3 months"
}

print(my_dict)
print(my_dict["fees"])

# for loop in dictionary :

for a in my_dict :
    print(a)
    print(my_dict[a])

